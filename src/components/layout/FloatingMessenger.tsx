export default function FloatingMessenger() {
  const [isOpen, setIsOpen] = useState(false);

  const channels = [
    {
      name: "Viber",
      href: `viber://chat?number=%2B${STORE.phoneRaw}`,
      bg: "bg-[#7360F2]",
      Icon: ViberIcon,
    },
    {
      name: "WhatsApp",
      href: `https://wa.me/${STORE.phoneRaw}`,
      bg: "bg-[#25D366]",
      Icon: WhatsAppIcon,
    },
    {
      name: "Telegram",
      href: `https://t.me/+${STORE.phoneRaw}`,
      bg: "bg-[#229ED9]",
      Icon: TelegramIcon,
    },
  ];

  return (
    <div className="fixed bottom-6 right-6 z-40 flex flex-col items-end gap-3">
      {/* Канали месенджерів */}
      <div
        className={`flex flex-col items-end gap-3 transition-all duration-300 ${
          isOpen ? "opacity-100 translate-y-0 pointer-events-auto" : "opacity-0 translate-y-4 pointer-events-none"
        }`}
      >
        {channels.map(({ name, href, bg, Icon }) => (
          <a
            key={name}
            href={href}
            target="_blank"
            rel="noopener noreferrer"
            onClick={() => trackEvent("Клік по месенджеру", { channel: name })}
            aria-label={`Написати у ${name}`}
            title={`${name}: ${STORE.phoneDisplay}`}
            className={`flex items-center justify-center w-12 h-12 rounded-full text-white shadow-lg hover:scale-110 transition-transform ${bg}`}
          >
            <Icon className="w-6 h-6" />
          </a>
        ))}
      </div>

      {/* Головна кнопка */}
      <button
        onClick={() => {
          setIsOpen((v) => {
            if (!v) trackEvent("Відкрито месенджери");
            return !v;
          });
        }}
        aria-label={isOpen ? "Закрити месенджери" : "Зв'язатися з нами"}
        aria-expanded={isOpen}
        className="flex items-center justify-center w-14 h-14 rounded-full bg-opora-brown text-white shadow-xl hover:opacity-90 transition-all"
      >
        {isOpen ? (
          <svg width="18" height="18" viewBox="0 0 14 14" fill="none" xmlns="http://www.w3.org/2000/svg">
            <path d="M1 1L13 13M1 13L13 1" stroke="white" strokeWidth="2" strokeLinecap="round" />
          </svg>
        ) : (
          <svg width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="1.8" strokeLinecap="round" strokeLinejoin="round">
            <path d="M21 11.5a8.38 8.38 0 01-.9 3.8 8.5 8.5 0 01-7.6 4.7 8.38 8.38 0 01-3.8-.9L3 21l1.9-5.7a8.38 8.38 0 01-.9-3.8 8.5 8.5 0 014.7-7.6 8.38 8.38 0 013.8-.9h.5a8.48 8.48 0 018 8v.5z" />
          </svg>
        )}
      </button>
    </div>
  );
}
